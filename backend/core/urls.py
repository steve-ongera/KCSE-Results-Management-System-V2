"""
apps/examinations/urls.py

URL routing for the examinations app.
All routes are prefixed with /api/v1/ by the project-level urls.py.
"""

from django.urls import path, include
from rest_framework.routers import DefaultRouter

from .views import (
    # Public
    ResultsLookupView,

    # Shared (read)
    ExaminationYearListView,
    ExaminationYearDetailView,
    ExaminationCenterListView,
    ExaminationCenterDetailView,
    SubjectListView,

    # Candidates
    CandidateViewSet,

    # Scripts
    ExaminationScriptViewSet,

    # Marks
    MarksEntryViewSet,

    # Results processing & publication
    ProcessResultsView,
    PublishResultsView,
    ResultPublicationDetailView,
    CandidateResultListView,
    CandidateResultDetailView,

    # Analytics
    SchoolPerformanceView,
    NationalStatisticsView,

    # Audit
    AuditLogListView,
)

# ── Router (ViewSets) ────────────────────────────────────────────────────────
router = DefaultRouter()
router.register(r'candidates',  CandidateViewSet,          basename='candidate')
router.register(r'scripts',     ExaminationScriptViewSet,  basename='script')
router.register(r'marks',       MarksEntryViewSet,         basename='marks')

# ── URL Patterns ─────────────────────────────────────────────────────────────
urlpatterns = [

    # ── ViewSet routes (auto-generated by router) ────────────────────────────
    path('', include(router.urls)),

    # ── Public — Results Lookup (no authentication) ──────────────────────────
    #   POST /api/v1/results/lookup/
    path(
        'results/lookup/',
        ResultsLookupView.as_view(),
        name='results-lookup',
    ),

    # ── Results — Staff views ────────────────────────────────────────────────
    #   GET  /api/v1/results/candidates/
    path(
        'results/candidates/',
        CandidateResultListView.as_view(),
        name='candidate-result-list',
    ),
    #   GET/PATCH /api/v1/results/candidates/{id}/
    path(
        'results/candidates/<uuid:pk>/',
        CandidateResultDetailView.as_view(),
        name='candidate-result-detail',
    ),

    # ── Results — Processing & Publication (KNEC admin) ──────────────────────
    #   POST /api/v1/results/process/
    path(
        'results/process/',
        ProcessResultsView.as_view(),
        name='results-process',
    ),
    #   POST /api/v1/results/publish/
    path(
        'results/publish/',
        PublishResultsView.as_view(),
        name='results-publish',
    ),
    #   GET/PATCH /api/v1/results/publication/{id}/
    path(
        'results/publication/<uuid:pk>/',
        ResultPublicationDetailView.as_view(),
        name='result-publication-detail',
    ),

    # ── Examination Years ────────────────────────────────────────────────────
    #   GET  /api/v1/years/
    path(
        'years/',
        ExaminationYearListView.as_view(),
        name='year-list',
    ),
    #   GET/PATCH /api/v1/years/{id}/
    path(
        'years/<uuid:pk>/',
        ExaminationYearDetailView.as_view(),
        name='year-detail',
    ),

    # ── Examination Centers ──────────────────────────────────────────────────
    #   GET/POST /api/v1/centers/
    path(
        'centers/',
        ExaminationCenterListView.as_view(),
        name='center-list',
    ),
    #   GET/PUT/PATCH/DELETE /api/v1/centers/{id}/
    path(
        'centers/<uuid:pk>/',
        ExaminationCenterDetailView.as_view(),
        name='center-detail',
    ),

    # ── Subjects ─────────────────────────────────────────────────────────────
    #   GET /api/v1/subjects/
    path(
        'subjects/',
        SubjectListView.as_view(),
        name='subject-list',
    ),

    # ── Analytics ────────────────────────────────────────────────────────────
    #   GET /api/v1/analytics/school/{center_code}/
    path(
        'analytics/school/<str:center_code>/',
        SchoolPerformanceView.as_view(),
        name='school-performance',
    ),
    #   GET /api/v1/analytics/national/
    path(
        'analytics/national/',
        NationalStatisticsView.as_view(),
        name='national-statistics',
    ),

    # ── Audit Logs ───────────────────────────────────────────────────────────
    #   GET /api/v1/audit-logs/
    path(
        'audit-logs/',
        AuditLogListView.as_view(),
        name='audit-log-list',
    ),
]